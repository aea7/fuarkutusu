export function t(key, lang = 'en') {
  const keys = key.split('.')
  let value = translations[lang]

  for (const k of keys) {
    if (value && typeof value === 'object') {
      value = value[k]
    } else {
      return key // Return key if translation not found
    }
  }

  return value || key
}
